num2 = 4

# Addition
sum = num1 + num2
print("Sum:", sum)

# Subtraction
diff = num1 - num2
print("Difference:", diff)

# Multiplication
product = num1 * num2
print("Product:", product)

# Division
quotient = num1 / num2
print("Quotient:", quotient)

# Integer Division (Floor Division)
quotient_int = num1 // num2
print("Quotient (Integer Division):", quotient_int)

# Modulo (Remainder)
remainder = num1 % num2
print("Remainder:", remainder)

# Exponentiation
exponent = num1 ** num2
