
weather = input("What is the weather like today? (sunny/rainy/cloudy): ")

if weather == "sunny":
    print("It's a sunny day! Let's go for a picnic.")
elif weather == "rainy":
    print("It's raining outside. Let's stay at home.")
elif weather == "cloudy":
    print("It's a cloudy day. We can still go for a picnic if you like.")
else:
    print("Sorry, I didn't understand the weather condition.")

